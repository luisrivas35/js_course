SELECT count(cantidad) as total_registros
FROM INSCRITOS;

-- pregunta 2
SELECT sum(cantidad) as total_inscritos
FROM INSCRITOS;

-- pregunta 3
SELECT *
FROM INSCRITOS
WHERE fecha = (SELECT MAX(fecha) FROM INSCRITOS);

-- pregunta 4
SELECT DATE(fecha) AS dia, SUM(cantidad) AS total_inscritos_por_dia
FROM INSCRITOS
WHERE fecha >= '01/01/2021'
GROUP BY DATE(fecha)
ORDER BY dia;

-- pregunta 5
SELECT DATE(fecha) AS dia, SUM(cantidad) AS total_inscritos_por_dia
FROM INSCRITOS
GROUP BY DATE(fecha)
ORDER BY total_inscritos_por_dia DESC
LIMIT 1;

